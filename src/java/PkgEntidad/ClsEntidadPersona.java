public class ClsEntidadPersona {
    String IdTipoDocumento;
    String NumeroDocumentoPersona;
    String ApellidoPrimeroPersona;
    String ApellidoSegundoPersona;
    String NombresPersona;
    String IdPais;
    String IdOcupacion;
    
    public String getIdTipoDocumento() {
        return IdTipoDocumento;
    }

    public void setIdTipoDocumento(String IdTipoDocumento) {
        this.IdTipoDocumento = IdTipoDocumento;
    }

    public String getNumeroDocumentoPersona() {
        return NumeroDocumentoPersona;
    }

    public void setNumeroDocumentoPersona(String NumeroDocumentoPersona) {
        this.NumeroDocumentoPersona = NumeroDocumentoPersona;
    }

    public String getApellidoPrimeroPersona() {
        return ApellidoPrimeroPersona;
    }

    public void setApellidoPrimeroPersona(String ApellidoPrimeroPersona) {
        this.ApellidoPrimeroPersona = ApellidoPrimeroPersona;
    }

    public String getApellidoSegundoPersona() {
        return ApellidoSegundoPersona;
    }

    public void setApellidoSegundoPersona(String ApellidoSegundoPersona) {
        this.ApellidoSegundoPersona = ApellidoSegundoPersona;
    }

    public String getNombresPersona() {
        return NombresPersona;
    }

    public void setNombresPersona(String NombresPersona) {
        this.NombresPersona = NombresPersona;
    }

    public String getIdPais() {
        return IdPais;
    }

    public void setIdPais(String IdPais) {
        this.IdPais = IdPais;
    }

    public String getIdOcupacion() {
        return IdOcupacion;
    }

    public void setIdOcupacion(String IdOcupacion) {
        this.IdOcupacion = IdOcupacion;
    }
    
}
